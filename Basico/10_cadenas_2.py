cadena = "el perro corre rapido"
cadena_corta = "EL PERRO CORRE RAPIDO"

#lower | Pasar a minusculas
print(cadena_corta.lower())
# islower | Verifica si una cadena se encuentra en minusculas.
print(cadena.islower())
print(cadena_corta.islower())
#upper | Pasar a mayusculas
print(cadena.upper())
# isupper | Verifica si una cadena se encuentra en minusculas.
print(cadena.isupper())
print(cadena_corta.isupper())
#capitalize | Pone en mayusculas la primera letra de una cadena
print(cadena.capitalize())
#replace | Reemplazar catacteres
print(cadena.replace("o","-"))