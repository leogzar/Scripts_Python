#tupla = conjunto de elementos entre un parentesis
tupla = (1,5,3,5,6,8,7,4,5,3,2,1,5,6)

#count | numero de veces que se repite en numero
print (tupla.count(6))
#len | para saber la longitud, in | busca datos en la tupla
print (len(tupla))
print (9 in tupla)
print (1 in tupla)

# index
print (tupla.index(5))